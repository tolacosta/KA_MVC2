package model.dto;

public class Usertype {
	private int usertypeid;
	private String usertypename;
	private boolean viewable;
	private boolean commentable;
	private boolean postable;
	private boolean deleteable;
	private boolean userable;
	private int countusers;
	
	public Usertype(){
		super();
	}
	
	public Usertype(int usertypeid, String usertypename, boolean viewable,
			boolean commentable, boolean postable, boolean deleteable,
			boolean userable) {
		super();
		this.usertypeid = usertypeid;
		this.usertypename = usertypename;
		this.viewable = viewable;
		this.commentable = commentable;
		this.postable = postable;
		this.deleteable = deleteable;
		this.userable = userable;
	}
	
	public int getUsertypeid() {
		return usertypeid;
	}
	
	public void setUsertypeid(int usertypeid) {
		this.usertypeid = usertypeid;
	}
	
	public String getUsertypename() {
		return usertypename;
	}
	
	public void setUsertypename(String usertypename) {
		this.usertypename = usertypename;
	}
	
	public boolean isViewable() {
		return viewable;
	}
	
	public void setViewable(boolean viewable) {
		this.viewable = viewable;
	}
	
	public boolean isCommentable() {
		return commentable;
	}
	
	public void setCommentable(boolean commentable) {
		this.commentable = commentable;
	}
	
	public boolean isPostable() {
		return postable;
	}
	
	public void setPostable(boolean postable) {
		this.postable = postable;
	}
	
	public boolean isDeleteable() {
		return deleteable;
	}
	
	public void setDeleteable(boolean deleteable) {
		this.deleteable = deleteable;
	}
	
	public boolean isUserable() {
		return userable;
	}
	
	public void setUserable(boolean userable) {
		this.userable = userable;
	}
	
	public int getCountusers() {
		return countusers;
	}
	
	public void setCountusers(int countusers) {
		this.countusers = countusers;
	}
	
	
}
